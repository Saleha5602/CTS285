def cal_add(num1, num2):
    total = num1 + num2
    return  total
def cal_subtract(num1, num2):
    total = num1 - num2
    return  total
    

def cal_divide(num1, num2):
    if num2 == 0:
        print("Error: Division by zero")
    else:
        total = num1 / num2
        return total

def cal_multiply(num1, num2):
    total = num1 * num2
    return total